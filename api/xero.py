def _escape_where_value(value):
    """Escape a string for interpolation into a Xero ``where`` filter."""
    return value.replace('\\', '\\\\').replace('"', '\\"')


def _contact_display_name(user):
    name = f"{user.first_name} {user.last_name}".strip()
    return name or user.username


def find_or_create_contact(user):
    """Return the Xero ContactID for a customer, creating the contact if
    needed. Matches by email first (the stable key), then by display name."""
    email = (user.email or '').strip()
    if email:
        result = _tenant_call('GET', 'Contacts', params={
            'where': f'EmailAddress=="{_escape_where_value(email)}"',
        })
        contacts = result.get('Contacts') or []
        if contacts:
            return contacts[0]['ContactID']

    return find_or_create_contact_by_name(_contact_display_name(user), email=email)


def find_or_create_contact_by_name(name, email=''):
    """Return the Xero ContactID for a bare display name (e.g. an invoice in
    a dog's name), creating the contact if needed. The business can then
    attach an email address in Xero and send the invoice from there."""
    result = _tenant_call('GET', 'Contacts', params={
        'where': f'Name=="{_escape_where_value(name)}"',
    })
    contacts = result.get('Contacts') or []
    if contacts:
        return contacts[0]['ContactID']

    payload = {'Contacts': [{'Name': name}]}
    if email:
        payload['Contacts'][0]['EmailAddress'] = email
    result = _tenant_call('POST', 'Contacts', payload=payload)
    return result['Contacts'][0]['ContactID']


def create_invoice(invoice, contact_id):
    """Create an authorised ACCREC invoice in Xero from a local Invoice.

    ``LineAmountTypes: Inclusive`` keeps the app's gross total equal to the
    Xero total regardless of the org's default tax rate. Returns
    ``(InvoiceID, InvoiceNumber)``.
    """
    lines = [
        {
            'Description': line.description,
            'Quantity': line.quantity,
            'UnitAmount': float(line.unit_price),
        }
        for line in invoice.lines.all()
    ]
    payload = {
        'Invoices': [{
            'Type': 'ACCREC',
            'Contact': {'ContactID': contact_id},
            'Status': 'AUTHORISED',
            'LineAmountTypes': 'Inclusive',
            'Date': timezone.now().date().isoformat(),
            'DueDate': invoice.due_date.isoformat() if invoice.due_date else timezone.now().date().isoformat(),
            'Reference': f'Daycare {invoice.period_label}',
            'LineItems': lines,
        }],
    }
    result = _tenant_call('POST', 'Invoices', payload=payload)
    created = result['Invoices'][0]
    return created['InvoiceID'], created.get('InvoiceNumber', '')


def get_online_invoice_url(xero_invoice_id):
    """Return the customer-facing online invoice URL, or '' if unavailable."""
    result = _tenant_call('GET', f'Invoices/{xero_invoice_id}/OnlineInvoice')
    entries = result.get('OnlineInvoices') or []
    if entries:
        return entries[0].get('OnlineInvoiceUrl', '')
    return ''


def fetch_invoices(xero_invoice_ids):
    """Fetch invoices (with their Payments) by Xero id. Returns a list of
    invoice dicts. Callers chunk the ids; Xero caps the IDs filter length."""
    if not xero_invoice_ids:
        return []
    result = _tenant_call('GET', 'Invoices', params={'IDs': ','.join(xero_invoice_ids)})
    return result.get('Invoices') or []


def void_invoice(xero_invoice_id):
    """Void an invoice in Xero, mirroring an in-app void.

    Xero only allows voiding invoices with no payments applied — if payments
    exist there, this raises XeroError and the books need a manual credit
    note/refund instead.
    """
    _tenant_call('POST', 'Invoices', payload={
        'Invoices': [{'InvoiceID': xero_invoice_id, 'Status': 'VOIDED'}],
    })


def create_payment(xero_invoice_id, amount, payment_date, account_code):
    """Record a payment against a Xero invoice (used to mirror manual payments
    recorded in the app). Returns the Xero PaymentID."""
    payload = {
        'Payments': [{
            'Invoice': {'InvoiceID': xero_invoice_id},
            'Account': {'Code': account_code},
            'Date': payment_date.isoformat(),
            'Amount': float(amount),
        }],
    }
    result = _tenant_call('PUT', 'Payments', payload=payload)
    return result['Payments'][0]['PaymentID']
