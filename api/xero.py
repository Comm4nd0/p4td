# OAuth2 connection flow
# ---------------------------------------------------------------------------

def build_authorize_url():
    """Create the Xero consent URL for the superuser connect flow.

    Stores a fresh CSRF ``state`` on the connection singleton — the callback
    arrives as a bare browser GET with no session, so this state token is what
    authenticates it.
    """
    from .models import XeroConnection

    if not is_configured():
        raise XeroNotConnected('Xero client credentials are not configured on the server.')
    if not getattr(settings, 'XERO_REDIRECT_URI', ''):
        raise XeroNotConnected('XERO_REDIRECT_URI is not configured on the server.')

    conn = XeroConnection.load()
    conn.oauth_state = _secrets.token_urlsafe(32)
    conn.oauth_state_created_at = timezone.now()
    conn.save()

    query = urllib.parse.urlencode({
        'response_type': 'code',
        'client_id': settings.XERO_CLIENT_ID,
        'redirect_uri': settings.XERO_REDIRECT_URI,
        'scope': SCOPES,
        'state': conn.oauth_state,
    })
    return f'{AUTH_URL}?{query}'


def handle_callback(code, state):
    """Complete the OAuth2 authorize flow: validate state, exchange the code,
    resolve the tenant, and persist the connection. Returns the tenant name.
    """
    from .models import XeroConnection

    conn = XeroConnection.load()
    state_age_ok = (
        conn.oauth_state_created_at is not None
        and (timezone.now() - conn.oauth_state_created_at).total_seconds() < STATE_MAX_AGE_SECONDS
    )
    if not state or not conn.oauth_state or not _secrets.compare_digest(state, conn.oauth_state) or not state_age_ok:
        raise XeroAuthError('Invalid or expired authorisation state. Start the connect flow again.')

    # One-shot: clear the state before the exchange so a replayed redirect fails.
    conn.oauth_state = ''
    conn.oauth_state_created_at = None
    conn.save()

    tokens = _token_request({
        'grant_type': 'authorization_code',
        'code': code,
        'redirect_uri': settings.XERO_REDIRECT_URI,
    })

    connections = _api_request('GET', CONNECTIONS_URL, tokens['access_token'])
    if not connections:
        raise XeroError('The Xero account has no authorised organisations.')
    tenant = connections[0]

    conn.tenant_id = tenant.get('tenantId', '')
    conn.tenant_name = tenant.get('tenantName', '')
    conn.refresh_token = tokens.get('refresh_token', '')
    conn.access_token = tokens.get('access_token', '')
    conn.access_token_expires_at = timezone.now() + timezone.timedelta(seconds=int(tokens.get('expires_in', 1800)) - 60)
    conn.connected_at = timezone.now()
    conn.save()
    return conn.tenant_name


def disconnect():
    """Forget the stored Xero connection (tokens + tenant)."""
    from .models import XeroConnection

    conn = XeroConnection.load()
    conn.tenant_id = ''
    conn.tenant_name = ''
    conn.refresh_token = ''
    conn.access_token = ''
    conn.access_token_expires_at = None
    conn.oauth_state = ''
    conn.oauth_state_created_at = None
    conn.connected_at = None
    conn.save()


def get_access_token():
    """Return a valid access token, refreshing (and rotating) if needed.

    Xero refresh tokens are single-use: each refresh returns a NEW refresh
    token and invalidates the old one. Two workers refreshing concurrently
    would clobber each other, so the refresh runs under ``select_for_update``
    on the singleton row and re-checks expiry after acquiring the lock —
    the second worker just reads the token the first one stored.
    """
    from .models import XeroConnection

    conn = XeroConnection.load()
    if not conn.is_connected:
        raise XeroNotConnected('Xero is not connected.')

    now = timezone.now()
    if conn.access_token and conn.access_token_expires_at and conn.access_token_expires_at > now:
        return conn.access_token

    try:
        with transaction.atomic():
            conn = XeroConnection.objects.select_for_update().get(pk=1)
            if not conn.is_connected:
                raise XeroNotConnected('Xero is not connected.')
            now = timezone.now()
            # Another worker may have refreshed while we waited on the lock.
            if conn.access_token and conn.access_token_expires_at and conn.access_token_expires_at > now:
                return conn.access_token
            tokens = _token_request({
                'grant_type': 'refresh_token',
                'refresh_token': conn.refresh_token,
            })
            conn.access_token = tokens.get('access_token', '')
            # Persist the rotated single-use refresh token BEFORE releasing the lock.
            conn.refresh_token = tokens.get('refresh_token', conn.refresh_token)
            conn.access_token_expires_at = now + timezone.timedelta(seconds=int(tokens.get('expires_in', 1800)) - 60)
            conn.save()
            return conn.access_token
    except XeroAuthError:
        # Dead refresh token — clear the connection so is_connected flips false
        # and /api/xero/status/ tells the superuser to reconnect. Must happen
        # OUTSIDE the atomic block: the propagating exception rolls that back.
        conn = XeroConnection.load()
        conn.refresh_token = ''
        conn.access_token = ''
        conn.access_token_expires_at = None
        conn.save()
        raise


def _tenant_call(method, path, payload=None, params=None):
    """An authenticated Accounting API call against the connected tenant."""
    from .models import XeroConnection

    token = get_access_token()
    tenant_id = XeroConnection.load().tenant_id
    return _api_request(method, path, token, tenant_id, payload=payload, params=params)


