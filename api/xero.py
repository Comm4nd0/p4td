"""Xero accounting integration (OAuth2 + Accounting API).

The business runs its books in Xero. Monthly invoices generated by
``api.billing`` are pushed here as ACCREC sales invoices, owners pay via the
Xero online-invoice URL, and a periodic sync pulls payments back so the app's
ledger converges with Xero's.

Connection model: client id/secret live in env vars (``XERO_CLIENT_ID`` /
``XERO_CLIENT_SECRET``); the rotating refresh token + tenant id captured by
the one-time superuser consent flow live in the :class:`api.models.XeroConnection`
pk=1 singleton. Everything degrades gracefully — callers treat
:class:`XeroNotConnected` as "feature off".

Uses the Python standard library only (``urllib``) so no extra pip dependency
is introduced — adding one to a single requirements file breaks the prod
Docker build. All network I/O goes through :func:`_token_request` and
:func:`_api_request`, which tests patch directly.
"""
import base64
import json as _json
import logging
import secrets as _secrets
import urllib.error
import urllib.parse
import urllib.request

from django.conf import settings
from django.db import transaction
from django.utils import timezone

logger = logging.getLogger(__name__)

AUTH_URL = 'https://login.xero.com/identity/connect/authorize'
TOKEN_URL = 'https://identity.xero.com/connect/token'
CONNECTIONS_URL = 'https://api.xero.com/connections'
API_BASE = 'https://api.xero.com/api.xro/2.0'
SCOPES = 'offline_access accounting.transactions accounting.contacts'

# How long an in-flight authorize state stays valid.
STATE_MAX_AGE_SECONDS = 600


class XeroError(Exception):
    """A Xero API error the caller should surface or store."""


class XeroNotConnected(XeroError):
    """Xero is not configured/connected — callers treat the feature as off."""


class XeroAuthError(XeroError):
    """The stored credentials are dead (e.g. rotated refresh token lost);
    a superuser must reconnect."""


def is_configured():
    """Whether the env-var app credentials are present."""
    return bool(getattr(settings, 'XERO_CLIENT_ID', '') and getattr(settings, 'XERO_CLIENT_SECRET', ''))


# ---------------------------------------------------------------------------
# HTTP primitives — the ONLY functions that touch the network. Tests patch
# these (mirroring api.geocoding._fetch_postcodes_io).
# ---------------------------------------------------------------------------

def _token_request(form):
    """POST ``form`` to the Xero token endpoint with HTTP Basic app credentials.

    Returns the parsed JSON dict. Raises :class:`XeroAuthError` on
    ``invalid_grant`` (dead refresh token / bad code), :class:`XeroError` on
    anything else.
    """
    basic = base64.b64encode(
        f"{settings.XERO_CLIENT_ID}:{settings.XERO_CLIENT_SECRET}".encode()
    ).decode()
    req = urllib.request.Request(
        TOKEN_URL,
        data=urllib.parse.urlencode(form).encode(),
        headers={
            'Authorization': f'Basic {basic}',
            'Content-Type': 'application/x-www-form-urlencoded',
            'User-Agent': 'p4td-backend',
        },
        method='POST',
    )
    try:
        with urllib.request.urlopen(req, timeout=15) as resp:
            return _json.loads(resp.read().decode('utf-8'))
    except urllib.error.HTTPError as exc:
        try:
            detail = exc.read().decode('utf-8')
        except Exception:
            detail = ''
        if 'invalid_grant' in detail:
            raise XeroAuthError('Xero refused the stored credentials (invalid_grant) — reconnect Xero.')
        raise XeroError(f'Xero token endpoint error (HTTP {exc.code}).')
    except (urllib.error.URLError, TimeoutError, ValueError):
        raise XeroError('Could not reach the Xero token endpoint.')


def _api_request(method, path, access_token, tenant_id=None, payload=None, params=None):
    """Call the Xero API and return the parsed JSON dict.

    ``path`` is absolute (starts with http) or relative to :data:`API_BASE`.
    Raises :class:`XeroError` with the response detail on failure.
    """
    url = path if path.startswith('http') else f'{API_BASE}/{path.lstrip("/")}'
    if params:
        url = f'{url}?{urllib.parse.urlencode(params)}'
    headers = {
        'Authorization': f'Bearer {access_token}',
        'Accept': 'application/json',
        'User-Agent': 'p4td-backend',
    }
    if tenant_id:
        headers['xero-tenant-id'] = tenant_id
    data = None
    if payload is not None:
        data = _json.dumps(payload).encode('utf-8')
        headers['Content-Type'] = 'application/json'
    req = urllib.request.Request(url, data=data, headers=headers, method=method)
    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            body = resp.read().decode('utf-8')
            return _json.loads(body) if body else {}
    except urllib.error.HTTPError as exc:
        try:
            detail = exc.read().decode('utf-8')[:500]
        except Exception:
            detail = ''
        if exc.code == 401:
            raise XeroAuthError(f'Xero rejected the access token (HTTP 401). {detail}')
        raise XeroError(f'Xero API error (HTTP {exc.code}): {detail}')
    except (urllib.error.URLError, TimeoutError, ValueError):
        raise XeroError('Could not reach the Xero API.')


